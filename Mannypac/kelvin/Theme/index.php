					<p>In 2007, Pacquiao made his first attempt to enter politics, running for a seat in the House of Representatives of the Philippines. He was defeated by incumbent Rep. Darlene Antonino-Custodio, and returned to life as a full-time boxer. In 2009, however, Pacquiao formed a new Filipino political party, the People's Champ Movement, and again ran for a legislative seat. He won in a landslide, beating opponent Roy Chiongbian to become the Sarangani province representative in May 2010. 
			    	<p>Three years later, Pacquiao earned a second term after running unopposed for reelection, and in 2016, the boxing great again leveraged his popularity to win a seat as a Philippines senator.
			    	<p>Along with his athletic and political careers, Pacquiao has shown off his vocal abilities by releasing two albums and collaborating on other tracks. He has appeared in several movies, and starred in the Philippine sitcom Show Me Da Manny from 2009-11. A documentary about his life, Manny, was released in the United States in early 2015.
			    	<p>acquiao's wife, Jinkee, was elected vice governor of Sarangani in 2013. They have five children together.
